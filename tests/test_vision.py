def _mask(lines):
    height = max(32, len(lines))
    width = max(32, max(len(line) for line in lines))
    canvas = np.zeros((height, width), dtype=np.uint8)
    y0 = (height - len(lines)) // 2
    x0 = (width - max(len(line) for line in lines)) // 2
    for y, line in enumerate(lines):
        for x, char in enumerate(line):
            if char == "#":
                canvas[y0 + y, x0 + x] = 1
    return canvas


